    private void connectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectButtonActionPerformed
        if (connectButton.getText().equals("Connect")) {
            Port = SerialPort.getCommPort(serialPorts[selectCOM.getSelectedIndex()].getSystemPortName());
            System.out.println(selectCOM.getSelectedIndex());
            Port.setComPortTimeouts(SerialPort.TIMEOUT_SCANNER, 0, 0);
            if (Port.openPort()) {
                System.out.println("port open :" + Port.isOpen());
                connectButton.setText("Disconnet");
                selectCOM.setEnabled(false);

            }
            Thread thread = new Thread() {
                public void run() {
                    Scanner scanner = new Scanner(Port.getInputStream());
                    Random random = new Random();
                    int r = random.nextInt(30);
                    Calendar saveTime = new GregorianCalendar();
                    Calendar nowTime = null;
                    CalReading calReading = new CalReading();
                    while (scanner.hasNextLine()) {
                        try {
                            String a = scanner.nextLine();
                            int kk = calReading.getValue(a);
                            meter1.setVal(kk);
                            nowTime = new GregorianCalendar();
                            if ((kk > 50) || ((nowTime.getTimeInMillis() - saveTime.getTimeInMillis()) >= 300000)) {
                                DBController.saveMeterReading(kk);
                            }
                        } catch (Exception e) {
                            System.out.println(e);
                        }
                    }
                    scanner.close();
                }
            };
            thread.start();
        } else {
            System.out.println("close port :" + Port.closePort());
            connectButton.setText("Connect");
            selectCOM.setEnabled(true);

        }
    }//GEN-LAST:event_connectButtonActionPerformed

