    private void initRun(){
        comboShowTranformermeter.removeAllItems();
        comboRepairAddid.removeAllItems();
        comboViewRepair.removeAllItems();
        grapsIdTrasnfomer.removeAllItems();
        comboIdTransformer.removeAllItems();
        try {
            ResultSet resultSet=DBController.getTransformerId();
            while(resultSet.next()){
                comboShowTranformermeter.addItem(resultSet.getInt("idTransformer"));
                comboRepairAddid.addItem(resultSet.getInt("idTransformer"));
                comboViewRepair.addItem(resultSet.getInt("idTransformer"));
                grapsIdTrasnfomer.addItem(resultSet.getInt("idTransformer"));
                comboIdTransformer.addItem(resultSet.getInt("idTransformer"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error Restart\n" + ex.getMessage().split("\\(")[0]);
        }
    }

