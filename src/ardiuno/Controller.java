    private void createPDF(List<Image> images) throws DocumentException, MalformedURLException, IOException {
        FileOutputStream pdfFileout = null;
        try {
            File file = new File("itext.pdf");
            pdfFileout = new FileOutputStream(file);
            Document doc = new Document();
            try {
                PdfWriter.getInstance(doc, pdfFileout);
            } catch (DocumentException ex) {
                Logger.getLogger(Controller.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.addAuthor("QuicklyJava.com");
            doc.addTitle("This is title");
            doc.open();
            Paragraph para1 = new Paragraph();
            para1.add("Date :"+(String) jTable1.getValueAt(jTable1.getSelectedRow(), 1));
            Paragraph para2 = new Paragraph();
            para2.add((String) jTable1.getValueAt(jTable1.getSelectedRow(), 2));
            doc.add(para1);
            doc.add(para2);
            //Adding an online image
            
            for (Image image : images) {                
                doc.add(com.itextpdf.text.Image.getInstance(image,null));
            }
            //adding a local image and aligned RIGHT
            
            
            doc.close();
            pdfFileout.close();
            System.out.println("Success!");
            String email=JOptionPane.showInputDialog(this, "Email");
            sendEmail(email,file);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Controller.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                pdfFileout.close();
            } catch (IOException ex) {
                Logger.getLogger(Controller.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

